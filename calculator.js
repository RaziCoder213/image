// Calculator functionality
function updateSubcategories() {
  const categorySelect = document.getElementById("category");
  const subcategorySelect = document.getElementById("subcategory");
  const selectedCategory = categorySelect.value;

  subcategorySelect.innerHTML = '<option value="">Select Subcategory</option>';

  if (selectedCategory && pricingData[selectedCategory]) {
    const subcategories = Object.keys(pricingData[selectedCategory]).filter(key => key !== "unit");
    
    subcategories.forEach((subcategory) => {
      const option = document.createElement("option");
      option.value = subcategory;
      option.textContent = subcategory;
      subcategorySelect.appendChild(option);
    });
  }
}

function calculatePrice() {
  const category = document.getElementById("category").value;
  const subcategory = document.getElementById("subcategory").value;
  const educationLevel = document.getElementById("education-level").value;
  const urgency = document.getElementById("urgency").value;
  const currency = document.getElementById("currency").value;
  const quantity = parseFloat(document.getElementById("quantity").value) || 1;

  if (!category || !subcategory || !educationLevel || !urgency) {
    document.getElementById("result").innerHTML =
      "<p>Please fill in all fields.</p>";
    return;
  }

  const pricing = pricingData[category][subcategory];
  if (!pricing) {
    document.getElementById("result").innerHTML =
      "<p>Pricing not available for this selection.</p>";
    return;
  }

  const unitPrice = pricing[educationLevel][urgency][currency];
  const totalPrice = (unitPrice * quantity).toFixed(2);
  const unit = pricing.unit;

  document.getElementById("result").innerHTML = `
    <h3>Price Calculation</h3>
    <p><strong>Category:</strong> ${category}</p>
    <p><strong>Subcategory:</strong> ${subcategory}</p>
    <p><strong>Education Level:</strong> ${educationLevel}</p>
    <p><strong>Urgency:</strong> ${urgency}</p>
    <p><strong>Quantity:</strong> ${quantity} ${unit}</p>
    <p><strong>Unit Price:</strong> ${currency} ${unitPrice}</p>
    <p><strong>Total Price:</strong> ${currency} ${totalPrice}</p>
  `;
}

// Event listeners
document
  .getElementById("category")
  .addEventListener("change", updateSubcategories);
document
  .getElementById("calculate-btn")
  .addEventListener("click", calculatePrice);

// Initialize subcategories on page load
updateSubcategories();
